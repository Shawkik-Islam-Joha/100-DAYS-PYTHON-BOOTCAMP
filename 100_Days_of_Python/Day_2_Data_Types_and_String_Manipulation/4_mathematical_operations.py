# Python follows the order PEMDAS (Parenthesis,Exponent,Multiplication,Division,Addition,Subtraction)

print("My age: " + str(12))      # String concatenation and type conversion
print(123 + 456)                 # Addition
print(7 - 3)                     # Subtraction
print(3 * 2)                     # Multiplication
print(5 / 3)                     # Division (float result)
print(5 // 3)                    # Floor division (integer result)
print(2 ** 3)                    # Exponentiation


